// Map checks if z exists in a map
func Map() bool {
	_, exists := zMap['z']
	return exists
}

// Array checks if z exists in a rune slice
func Array() bool {
	for currentRune := range runes {
		if currentRune == 'z' {
			return true
		}
	}
	return false
}
